    public void Listar() {
        
        try {
            AlunoDao dao = new AlunoDao();
            List<Alunos> listarAlunos = dao.listarAlunos();
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setNumRows(0);
            
            for (Alunos la : listarAlunos){
                modelo.addRow(new Object[]{
                    la.getMatricula(),
                    la.getNome(),
                    la.getEmail(),
                    la.getCelular(),
                    la.getTelefone(),
                    la.getObs(),
                });
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao listar alunos!");
        }
    }
