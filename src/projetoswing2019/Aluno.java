    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        try {
            Alunos obj = new Alunos();
            
            obj.setNome(tfNome.getText());
            obj.setEmail(tfEmail.getText());
            obj.setTelefone(Integer.parseInt(tfTelefone.getText()));
            obj.setCelular(Integer.parseInt(tfCelular.getText()));
            obj.setObs(tfObservacao.getText());
            
            dao = new AlunoDao();
            dao.cadastraAluno(obj);
            
            JOptionPane.showMessageDialog(null, "Cadastro realizado");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

